<?php
/**
 * This file contains the `SupportQueryRequiredFieldsException` class that defines
 * an exception when one or more required fields (email / query) are empty at the
 * time of raising a support query.
 *
 * @package miniorange-wp-as-saml-idp\exception
 */

namespace IDP\Exception;

if ( ! defined( 'ABSPATH' ) ) {
	exit;
}

use IDP\Helper\Constants\MoIDPMessages;

/**
 * Exception denotes that one or more required fields (email / query)
 * are empty at the time of raising a support query.
 */
class SupportQueryRequiredFieldsException extends \Exception {

	/**
	 * Constructor function, which defines the `$code` and `$message` for
	 * the exception, and makes a call to the parent (`Exception`) constructor.
	 */
	public function __construct() {
		$message = MoIDPMessages::show_message( 'REQUIRED_QUERY_FIELDS' );
		$code    = 109;
		parent::__construct( $message, $code, null );
	}

	/**
	 * Returns the string representation of the exception,
	 * with exception code and the error message.
	 *
	 * @return string
	 */
	public function __toString() {
		return __CLASS__ . ": [{$this->code}]: {$this->message}\n";
	}
}
